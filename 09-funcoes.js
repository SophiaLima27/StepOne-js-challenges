// Crie as seguintes funções:

// 1- Função de soma, com o nome ‘sum', que retorna a soma de dois números.

function sum(a, b) {
    const addition = a + b;
    return addition;
}

let sumOfNumbers = sum(28, 26);
console.log ("The result of sum is " + sumOfNumbers);

// 2- Função de subtração, com o nome ‘subtract’, que retorna a subtração de dois números

function subtract (a, b) {
    const subtract = a - b;
    return subtract;
}

let subtractOfNumbers = subtract (28, 26);
console.log ("The result of subtract is " + subtractOfNumbers);

// 3- Função de multiplicação, com o nome ‘multiplication, que retorna 
// a multiplicação de dois números

function multiplication (a, b) {
    const multiplication = a * b;
    return multiplication;
}

let multiplicationOfNumbers = multiplication (28, 26);
console.log ("The result of multiplication is " + multiplicationOfNumbers);

  
