// Utilizando o for, faça uma estrutura de repetição que dê o console dos números 100 a 1.

for (let i = 50; i<=100;i++) {
    console.log (i);
}

for (let s=2; s<=100; s = s + 2) {
    console.log (s);
}

for (let t = 100; t>=1; t--) {
    console.log (t);
}
